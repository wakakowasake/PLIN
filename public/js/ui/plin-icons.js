function escapeAttribute(value) {
    return String(value || '')
        .replace(/&/g, '&amp;')
        .replace(/"/g, '&quot;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;');
}

export function plinIcon(name, { className = '', label = '', strokeWidth = 2 } = {}) {
    const nodes = ICON_NODES[name] || ICON_NODES['map-pin-plus'];
    const safeClassName = escapeAttribute(['plin-lucide-icon', className].filter(Boolean).join(' '));
    const accessibility = label
        ? `role="img" aria-label="${escapeAttribute(label)}"`
        : 'aria-hidden="true"';

    return `<svg xmlns="http://www.w3.org/2000/svg" width="1em" height="1em" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="${Number(strokeWidth) || 2}" stroke-linecap="round" stroke-linejoin="round" focusable="false" class="${safeClassName}" ${accessibility}>${nodes.join('')}</svg>`;
}

export function hydratePlinIcons(root = document) {
    root.querySelectorAll('[data-plin-icon]').forEach((target) => {
        const name = target.getAttribute('data-plin-icon');
        if (!name) return;

        const template = document.createElement('template');
        template.innerHTML = plinIcon(name, {
            className: target.getAttribute('class') || '',
            label: target.getAttribute('aria-label') || '',
            strokeWidth: Number(target.getAttribute('data-plin-icon-stroke') || 2)
        });

        const icon = template.content.firstElementChild;
        if (icon) {
            target.replaceWith(icon);
        }
    });
}
