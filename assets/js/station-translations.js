export function translateLine(japaneseName) {
    // 일본어가 아니면 그대로 반환 (한국어/영어 등)
    // 일본어 문자 체크: 히라가나(\u3040-\u309F), 카타카나(\u30A0-\u30FF), 한자(\u4E00-\u9FAF)
    const hasJapanese = /[\u3040-\u309F\u30A0-\u30FF\u4E00-\u9FAF]/.test(japaneseName);
    if (!hasJapanese) {
        return japaneseName;  // 한국어 "7호선", 영어 "501" 등은 그대로 반환
    }
    
    // ★ 먼저 회사명 제거 (OsakaMetro, 東京メトロ, ＪＲ 등)
    let lineName = japaneseName
        .replace(/^OsakaMetro/, '')
        .replace(/^東京メトロ/, '')
        .replace(/^ＪＲ/, '')
        .replace(/^JR/, '')
        .replace(/^近鉄/, '')
        .replace(/^阪急/, '')
        .replace(/^阪神/, '')
        .replace(/外回り$/, '(외선)')
        .replace(/内回り$/, '(내선)');
    
    // 회사명 제거 후 전체 일치 번역 시도
    if (lineTranslations[lineName]) {
        return lineTranslations[lineName];
    }
    
    // 원본으로도 시도 (회사명 없는 경우)
    if (lineTranslations[japaneseName]) {
        return lineTranslations[japaneseName];
    }
    
    // 주요 노선명 부분 매칭 번역
    const lineNameMap = {
        '御堂筋線': '미도스지선',
        '谷町線': '타니마치선',
        '中央線': '츄오선',
        '四つ橋線': '요츠바시선',
        '千日前線': '센니치마에선',
        '堺筋線': '사카이스지선',
        '奈良線': '나라선',
        '難波線': '난바선',
        '京都線': '교토선',
        '神戸線': '고베선',
        '本線': '본선',
        '大阪環状線': '오사카 간조선',
        '東海道本線': '도카이도 본선',
        '山手線': '야마노테선',
        '銀座線': '긴자선',
        '丸ノ内線': '마루노우치선',
        '日比谷線': '히비야선',
        '東西線': '도자이선',
        '千代田線': '치요다선',
        '半蔵門線': '한조몬선'
    };
    
    for (const [ja, ko] of Object.entries(lineNameMap)) {
        if (lineName.includes(ja)) {
            lineName = lineName.replace(ja, ko);
        }
    }
    
    return lineName || japaneseName;
}
