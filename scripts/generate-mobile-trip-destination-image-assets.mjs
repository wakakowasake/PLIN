import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const manifestPath = path.join(
    repoRoot,
    'public',
    'static',
    'images',
    'trip-destinations',
    'destination-image-file-list.json'
);
const outputPath = path.join(
    repoRoot,
    'apps',
    'mobile',
    'src',
    'generated',
    'tripDestinationImageAssets.ts'
);
const outputDir = path.dirname(outputPath);

function toPosixPath(value) {
    return value.split(path.sep).join('/');
}

const manifestRaw = await fs.readFile(manifestPath, 'utf8');
const manifestEntries = JSON.parse(manifestRaw);

if (!Array.isArray(manifestEntries)) {
    throw new Error('destination-image-file-list.json 형식이 올바르지 않아요.');
}

const imageEntries = manifestEntries
    .map((entry) => ({
        id: String(entry?.id || '').trim().toLowerCase(),
        filename: String(entry?.filename || '').trim()
    }))
    .filter((entry) => entry.id && entry.filename)
    .sort((left, right) => left.id.localeCompare(right.id));

const defaultImagePath = toPosixPath(path.relative(
    outputDir,
    path.join(repoRoot, 'public', 'static', 'images', 'trip-destinations', 'default.jpg')
));

const lines = [
    '/* eslint-disable global-require */',
    "import type { ImageSourcePropType } from 'react-native';",
    '',
    '// This file is generated by scripts/generate-mobile-trip-destination-image-assets.mjs',
    `export const defaultTripDestinationImageSource: ImageSourcePropType = require(${JSON.stringify(defaultImagePath)}) as ImageSourcePropType;`,
    '',
    'export const tripDestinationImageSourceById: Record<string, ImageSourcePropType> = {',
    ...imageEntries.map((entry) => {
        const relativeImagePath = toPosixPath(path.relative(
            outputDir,
            path.join(repoRoot, 'public', 'static', 'images', 'trip-destinations', entry.filename)
        ));

        return `    ${JSON.stringify(entry.id)}: require(${JSON.stringify(relativeImagePath)}) as ImageSourcePropType,`;
    }),
    '};',
    '',
    'export function getTripDestinationImageSource(id: string | null | undefined): ImageSourcePropType {',
    "    const normalizedId = String(id || '').trim().toLowerCase();",
    '    return tripDestinationImageSourceById[normalizedId] || defaultTripDestinationImageSource;',
    '}',
    ''
];

await fs.mkdir(outputDir, { recursive: true });
await fs.writeFile(outputPath, lines.join('\n'));

console.log(`Generated ${path.relative(repoRoot, outputPath)} with ${imageEntries.length} entries.`);
